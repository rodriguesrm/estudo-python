"""
Python = Linguagem de programação
Tipagem = Dinâmica / Forte
str -> string -> text
String são textos que estão dentro de aspas
"""

# Aspas simples
print('AS =>', 'Rodrigo Rodrigues')
print('AS =>', 'Rodrigo "Rodrigues"')

# Aspas dupla
print("AD =>", "Rodrigo Rodrigues")
print("AD =>", "Rodrigo 'Rodrigues'")

# Espace & r
print(" E =>", "Rodrigo \"Rodrigues\"")
print(" R =>", r"Rodrigo \"Rodrigues\"")